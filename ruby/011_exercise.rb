Exercise 011
I. Write a ruby script that will:
  1. prompt the user to enter two or more integers
  2. calculate the sum of all entered integers
  3. print the sum every step
  4. quit the application if the user enters 'q'

II. Write a ruby script that will:
  1. prompt the user to enter an addition of many numbers (10 + 23 + 33 + 44 ....) on one line
  2. calculate the sum of all entered integers
  3. print the sum
  4. repeat step 1 unless 'q' is entered



